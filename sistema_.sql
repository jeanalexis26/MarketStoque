# 🛒 MarquetStoque

*MarquetStoque* é um sistema de gerenciamento de estoque desenvolvido para supermercados e mercearias.  
Seu objetivo é facilitar o controle de produtos, entradas e saídas, validade e fornecedores, garantindo uma gestão simples, moderna e eficiente.

---

## 🚀 Funcionalidades

- 📦 *Cadastro de produtos*  
  Nome, código, categoria, preço de compra e venda, validade e quantidade.

- 🔁 *Controle de estoque*  
  Atualização automática ao registrar entrada ou saída de produtos.

- 📅 *Controle de validade*  
  Alertas automáticos para produtos vencidos ou próximos da validade.

- 🧾 *Cadastro de fornecedores*  
  Nome, CNPJ, telefone e endereço.

- 🔐 *Login de usuários*  
  Controle de acesso com níveis (administrador e funcionário).

- 📊 *Relatórios e estatísticas*  
  Itens com baixo estoque, produtos mais vendidos e histórico de movimentações.

